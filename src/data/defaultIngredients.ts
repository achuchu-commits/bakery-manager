export function buildDefaultIngredients(userId: string): Omit<IngredientInventoryItem, 'id'>[] {
  const today = new Date().toISOString().split('T')[0];
  return RAW.map(([category, name, vendor, spec, price, unit]) => ({
    userId,
    name,
    vendor,
    spec,
    price,
    unit,
    unitPrice: Math.round((price / spec) * 1000) / 1000,
    purchaseDate: today,
    category,
  }));
}
